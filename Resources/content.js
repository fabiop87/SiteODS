const odsContentContainer = document.getElementById("ods-content");

function loadODSContent(odsId) {
    if (!odsId || !odsContent[odsId]) {
        // Mostrar conteúdo padrão ou mensagem de boas-vindas aqui
        odsContentContainer.innerHTML = `
          <h2>Bem-vindo aos Objetivos de Desenvolvimento Sustentável</h2>
          <p>Escolha um dos ODS na barra lateral para obter mais informações.</p>
        `;
    } else {
        const ods = odsContent[odsId];
        odsContentContainer.innerHTML = `
      <h2>${ods.title}</h2>
      <p>${ods.content}</p>
      <img src="${ods.image}" alt="${ods.title} Image" class="img-fluid">
    `;
    }
}


document.addEventListener("DOMContentLoaded", () => {
    const navLinks = document.querySelectorAll(".nav-link");
    navLinks.forEach(link => {
        link.addEventListener("click", (event) => {
            event.preventDefault();
            const odsId = link.getAttribute("href").substring(1);
            loadODSContent(odsId);
        });
    });
});
